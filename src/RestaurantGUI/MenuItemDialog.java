    private void searchButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_searchButtonActionPerformed
        // TODO add your handling code here:

        // read the String from the text box and parse as int
        try {
            int itemNo = Integer.parseInt(itemTF.getText());

            // find the item with this itemNo
            theItem = theRestaurant.findMenuItem(itemNo);
            if (theItem == null) {
                // alert
                int opt = JOptionPane.showConfirmDialog(this, "Item Not Found. Create New Item?", "Not Found", JOptionPane.YES_NO_OPTION);
                if (opt == JOptionPane.YES_OPTION) {
                    itemTF.setText("" + MenuItem.getNextNo());
                    itemTF.setEnabled(false);
                    descriptionTF.setEnabled(true);
                    priceTF.setEnabled(true);
                    okButton.setEnabled(true);
                    searchButton.setEnabled(false);
                }
            } else // menu item found
            {
                descriptionTF.setText(theItem.getDescription());
                priceTF.setText("" + theItem.getPrice());

                // enable for editing
                descriptionTF.setEnabled(true);
                priceTF.setEnabled(true);
                okButton.setEnabled(true);
                itemTF.setEnabled(false);
                searchButton.setEnabled(false);
            }
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Item Number must be a number!");
        }
    }//GEN-LAST:event_searchButtonActionPerformed

