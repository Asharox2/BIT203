    private void okButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_okButtonActionPerformed
        // TODO add your handling code here:

        try {
            boolean valid = false;

            String description = descriptionTF.getText();
            double price = Double.parseDouble(priceTF.getText());
            if (description.equals("")) {
                JOptionPane.showMessageDialog(this, "Description cannot be blank");
            } else if (price < 0) {
                JOptionPane.showMessageDialog(this, "Price cannot be negative");
            } else {
                valid = true;
            }
            if (valid) {
                if (theItem != null) {
                    theRestaurant.updateItem(theItem, description, price);
                } else {
                    theRestaurant.addMenuItem(description, price);
                } 
                this.dispose();
            }

           
        } catch (NumberFormatException nfe) {
            JOptionPane.showMessageDialog(this, "Price Must Be Numeric");
        }
    }//GEN-LAST:event_okButtonActionPerformed

